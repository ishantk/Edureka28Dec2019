package co.edureka.javalang;

public class MathAPI {

	public static void main(String[] args) {
		
		System.out.println(">> PI is: "+Math.PI);
		System.out.println(">> SquareRoot of 16 is: "+Math.sqrt(16));
		System.out.println(">> Log Base 10 for 100 is: "+Math.log10(100));
		System.out.println(Math.sin(10));
		System.out.println(Math.cos(10));
		System.out.println(">> Floor of 5.6 is: "+Math.floor(5.6));
		System.out.println(">> Ceil of 5.6 is: "+Math.ceil(5.6));
		
		// Math is a built in class with static methods
		// Since it is available as a part of java.lang package we need not to import it :)
		// Thread was also not imported as it was part of java.lang package
		
		// java.lang is a fundamental package with so many built in classes for us
		// we need not to import any of the class in java.lang package :)
		
		// PS: Explore more of Math APIs
		

	}

}
